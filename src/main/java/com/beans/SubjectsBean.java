package com.beans;

public class SubjectsBean {
	
	private int apogee;
	private double mark;
	
	public SubjectsBean() {
		super();
	}
	
	public SubjectsBean(int apogee, double mark) {
		super();
		this.apogee = apogee;
		this.mark = mark;
	}
	
	public int getApogee() {
		return apogee;
	}
	public void setApogee(int apogee) {
		this.apogee = apogee;
	}
	public double getMark() {
		return mark;
	}
	public void setMark(double mark) {
		this.mark = mark;
	}

	
	

}
